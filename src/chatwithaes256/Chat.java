    private void load_chat(){
        //menampilkan data database kedalam tabel
          
        try {
            int no=1;
            sql = "select u.name, c.message, c.salt, c.iv from chats c join users u on u.id = c.from where c.to = '"+this.to+"' and c.from = '"+this.from+"'";
            Config DB = new Config();
            java.sql.Statement stm= DB.configDB().createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                String message = aes.decrypt(res.getString("message"), "password", res.getString("salt"), res.getString("iv"));
                chatArea.append( res.getString("name") + ": " + message + "\n");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

