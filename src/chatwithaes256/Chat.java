            // Aes generate random Iv
            String genIV =   aes.generateIV("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789", 16);
            String tmpIV = genIV;

        // Aes encrypt phrase
